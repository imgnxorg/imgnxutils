.TH RENDR JS
.SH NAME
rendr \- Opens either a terminal in the browser or a browser in the terminal — at random.
.SH SYNOPSIS
. rendr
[/fR \fIBITS\fR]
[\fB\-\-bits\fR \fIBITS\fR]
.IR file ...
.SH DESCRIPTION
.B corrupt
modifies files by toggling a randomly chosen bit.
.SH OPTIONS
.TP
.BR \-n ", " \-\-bits =\fIBITS\fR
Set the number of bits to modify.
Default is one bit.
